#include <stdio.h>

int main() {
  
  int day = 1;

    switch (day) {
        case 1:
            printf("Monday\n");
            break;
        case 2:
            printf("Tuesday\n");
            break;
        case 3:
            printf("Wednesday\n");
            break;
        default:
            printf("Invalid day\n");
    }
    return 0;
}

int main() {
    int weather = 2;

    switch (weather) {
        case 1:
            printf("Sunny\n");
            break;
        case 2:
            printf("Rainy\n");
            break;
        case 3:
            printf("Cloudy\n");
            break;
        default:
            printf("Unknown weather\n");
    }
    return 0;
}

